<?php

class Minder_View_Helper_MinderDatePicker extends Zend_View_Helper_FormText {
    public function minderDatePicker($name, $value = null, $attribs = null) {
        $info = $this->_getInfo($name, $value, $attribs);
        extract($info); // name, value, attribs, options, listsep, disable

        // build the element
        $disabled = '';
        if ($disable) {
            // disabled
            $disabled = ' disabled="disabled"';
        }

        // XHTML or HTML end tag?
        $endTag = ' />';
        if (($this->view instanceof Zend_View_Abstract) && !$this->view->doctype()->isXhtml()) {
            $endTag= '>';
        }

        $xhtml = '<input sub-type="date-picker" type="text"'
                . ' name="' . $this->view->escape($name) . '"'
                . ' id="' . $this->view->escape($id) . '"'
                . ' value="' . $this->view->escape($value) . '"'
                . $disabled
                . $this->_htmlAttribs($attribs)
                . $endTag
                . '<script type="text/javascript">$(\'#' . $this->view->escape($id) . '\').datepicker({dateFormat: "yy/mm/dd"});</script>';


        return $xhtml;
    }
}